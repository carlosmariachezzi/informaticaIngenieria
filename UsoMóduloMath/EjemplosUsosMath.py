import math

# Número de ejemplo
x = 25

# Raíz cuadrada
raiz = math.sqrt(x)

# Potencia
potencia = math.pow(x, 2)

# Logaritmo natural
logaritmo = math.log(x)

# Constantes matemáticas
pi = math.pi
euler = math.e

# Funciones trigonométricas
angulo_grados = 45
angulo_radianes = math.radians(angulo_grados)

seno = math.sin(angulo_radianes)
coseno = math.cos(angulo_radianes)
tangente = math.tan(angulo_radianes)

# Mostrar resultados
print(f"Raíz cuadrada de {x}: {raiz}")
print(f"{x} al cuadrado: {potencia}")
print(f"Logaritmo natural de {x}: {logaritmo}")
print(f"Valor de π: {pi}")
print(f"Valor de e: {euler}")
print(f"Seno de {angulo_grados}°: {seno}")
print(f"Coseno de {angulo_grados}°: {coseno}")
print(f"Tangente de {angulo_grados}°: {tangente}")